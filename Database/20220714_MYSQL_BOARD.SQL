DROP TABLE BOARD;
CREATE TABLE BOARD (
 BOARD_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
 BOARD_TITLE VARCHAR(90) NOT NULL,
 BOARD_CONTENT VARCHAR(2000) NOT NULL,
 WRITE_DATE DATEtime DEFAULT NOW()

);

CREATE TABLE COMMENT_SECTION(
COMMENT_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
COMMENT_CONTENT VARCHAR(1500) NOT NULL,
COMMENT_AUTHOR VARCHAR(30) NOT NULL,
COMMENT_DATE DATETIME DEFAULT NOW()
);

CREATE TABLE ATTACH(
ATTACH_ID INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
ORIGINAL_FILE_NAME VARCHAR(300) NOT NULL,
SYSTEM_FILE_NAME VARCHAR(300) NOT NULL
);

CREATE TABLE MEMBER_INFO(
MEMBER_KEY INT UNSIGNED PRIMARY KEY,
MEMBER_ID VARCHAR(30) UNIQUE NOT NULL,
MEMBER_NAME VARCHAR(21) NOT NULL,
MEMBER_PHONE CHAR(15) UNIQUE,
MEMBER_REDDATE DATETIME
 DEFAULT NOW(),
DELETE_YN CHAR(2) NOT NULL DEFAULT 'N'
);


SET SESSION FOREIGN_KEY_CHECKS=0;

